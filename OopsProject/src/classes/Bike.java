package classes;

public class Bike extends Vehicle {

	private boolean isSportsBike;
	private boolean isPillionSeat;

	public boolean isSportsBike() {
		return isSportsBike;
	}

	public void setSportsBike(boolean isSportsBike) {
		this.isSportsBike = isSportsBike;
	}

	public boolean isPillionSeat() {
		return isPillionSeat;
	}

	public void setPillionSeat(boolean isPillionSeat) {
		this.isPillionSeat = isPillionSeat;
	}

}
